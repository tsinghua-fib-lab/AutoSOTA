"""
ProcessBench strict evaluation following the ProcessBench paper.

From ProcessBench paper and GitHub:
- For each step, get PRM score
- Apply threshold: if score < threshold → step predicted "incorrect"
- First step predicted as incorrect = predicted first error step
- If no step below threshold → predict "all correct" (-1)

F1 = harmonic mean of:
  acc_correct: fraction of correct solutions correctly classified as "all correct"
  acc_error: fraction of error solutions where first predicted error = true first error

Threshold is determined to maximize F1 on GSM8K, then applied to all datasets.
"""

import json
import torch
import torch.nn.functional as F
from transformers import AutoTokenizer, AutoModelForSequenceClassification
import argparse
from tqdm import tqdm
import numpy as np


def score_all_steps(problem, steps, model, tokenizer, device, max_length=1500):
    """Score each step individually. Return list of correct probabilities."""
    prompt = ("### Instruct:\nThe steps in 'Selected steps' are the correct problem-solving steps "
              "of the problem, while the steps in 'Next step:' are the next problem-solving steps "
              "generated by an AI agent based on the steps in 'Selected steps:'.You need to rate "
              "the step in 'Next step:' based on it`s usefulness and correctness.\n\n")

    correct_probs = []
    previous_steps = ""

    for i, step in enumerate(steps):
        # State: everything up to and including the current step's context
        state = prompt + "Problem:" + problem + "\nSelected steps:\n" + previous_steps
        # Next step to evaluate
        next_step_text = "Next step:Step " + str(i+1) + ": " + step + "\n"

        # Tokenize separately and concatenate (training format)
        tok_next = tokenizer(next_step_text, padding=False, max_length=max_length,
                             truncation=True, return_tensors="pt").input_ids[0]
        tok_state = tokenizer(state, padding=False,
                              max_length=max(max_length - len(tok_next), 1),
                              truncation=True, return_tensors="pt").input_ids[0]
        combined = torch.cat([tok_state, tok_next], dim=0).unsqueeze(0).to(device)
        attn = combined.ne(tokenizer.pad_token_id).to(device)

        with torch.no_grad():
            out = model(input_ids=combined, attention_mask=attn)

        # Multi-label classification: use softmax instead of sigmoid
        probs = torch.softmax(out.logits[0], dim=-1)
        correct_probs.append(float(probs[0].cpu()))

        # Update previous steps
        previous_steps += "Step " + str(i+1) + ": " + step + "\n"

    return correct_probs


def predict_label(correct_probs, threshold):
    """Predict the first error step. Returns -1 if all steps predicted correct."""
    for i, prob in enumerate(correct_probs):
        if prob < threshold:
            return i  # 0-indexed first error step (matches label format)
    return -1


def compute_metrics(all_correct_probs, labels, threshold):
    """
    Compute acc_correct and acc_error.
    acc_correct: fraction of correct solutions predicted as "all correct"
    acc_error: fraction of error solutions where predicted first error = true first error
    """
    n_correct = sum(1 for l in labels if l == -1)
    n_error = sum(1 for l in labels if l >= 0)

    if n_correct == 0 or n_error == 0:
        return 0.0, 0.0, 0.0

    n_correct_right = 0
    n_error_right = 0

    for probs, label in zip(all_correct_probs, labels):
        pred = predict_label(probs, threshold)
        if label == -1:
            if pred == -1:
                n_correct_right += 1
        else:
            if pred == label:
                n_error_right += 1

    acc_correct = n_correct_right / n_correct
    acc_error = n_error_right / n_error

    if acc_correct + acc_error == 0:
        return 0.0, acc_correct, acc_error
    f1 = 2 * acc_correct * acc_error / (acc_correct + acc_error)
    return f1, acc_correct, acc_error


def compute_binary_metrics(all_correct_probs, labels, threshold):
    """
    Binary F1: does the solution have any error or not?
    acc_correct: correct solutions predicted as all-correct
    acc_error: error solutions where any step below threshold
    """
    n_correct = sum(1 for l in labels if l == -1)
    n_error = sum(1 for l in labels if l >= 0)

    if n_correct == 0 or n_error == 0:
        return 0.0, 0.0, 0.0

    tp = sum(1 for probs, l in zip(all_correct_probs, labels)
             if l >= 0 and min(probs) < threshold)
    tn = sum(1 for probs, l in zip(all_correct_probs, labels)
             if l == -1 and min(probs) >= threshold)

    acc_error = tp / n_error
    acc_correct = tn / n_correct

    if acc_error + acc_correct == 0:
        return 0.0, acc_error, acc_correct
    f1 = 2 * acc_error * acc_correct / (acc_error + acc_correct)
    return f1, acc_error, acc_correct


def evaluate_dataset(data_path, model, tokenizer, device, dataset_name, max_length=1500):
    with open(data_path) as f:
        data = json.load(f)

    all_probs = []
    labels = []

    for item in tqdm(data, desc=dataset_name):
        prob_list = score_all_steps(
            item['problem'], item['steps'], model, tokenizer, device, max_length
        )
        all_probs.append(prob_list)
        labels.append(item['label'])

    # Find best threshold (maximizing strict F1)
    best_f1, best_thr = 0.0, 0.5
    for thr in np.arange(0.001, 1.0, 0.001):
        f1, _, _ = compute_metrics(all_probs, labels, thr)
        if f1 > best_f1:
            best_f1 = f1
            best_thr = thr

    f1_05, ae05, ac05 = compute_metrics(all_probs, labels, 0.5)
    f1_best, ae_best, ac_best = compute_metrics(all_probs, labels, best_thr)

    # Also compute binary F1
    bf1_05, bae05, bac05 = compute_binary_metrics(all_probs, labels, 0.5)
    bf1_best, bae_best, bac_best = compute_binary_metrics(all_probs, labels, best_thr)

    print(f"\n{dataset_name}:")
    print(f"  STRICT F1 (exact step match):")
    print(f"    threshold=0.50: F1={f1_05*100:.1f}  acc_error={ae05*100:.1f}  acc_correct={ac05*100:.1f}")
    print(f"    threshold={best_thr:.2f}: F1={f1_best*100:.1f}  acc_error={ae_best*100:.1f}  acc_correct={ac_best*100:.1f}")
    print(f"  BINARY F1 (any error detection):")
    print(f"    threshold=0.50: F1={bf1_05*100:.1f}  acc_error={bae05*100:.1f}  acc_correct={bac05*100:.1f}")
    print(f"    threshold={best_thr:.2f}: F1={bf1_best*100:.1f}  acc_error={bae_best*100:.1f}  acc_correct={bac_best*100:.1f}")

    return all_probs, labels, f1_best, best_thr


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--model_path", default="/checkpoints/EpicPRM_trained_checkpoint")
    parser.add_argument("--data_dir", default="/datasets/ProcessBench")
    parser.add_argument("--max_length", type=int, default=1500)
    args = parser.parse_args()

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print("Using device:", device)

    tokenizer = AutoTokenizer.from_pretrained(args.model_path)
    model = AutoModelForSequenceClassification.from_pretrained(
        args.model_path, num_labels=2, dtype=torch.bfloat16
    ).to(device)
    model.eval()
    print("Model loaded.")

    datasets = ["gsm8k", "math", "olympiadbench", "omnimath"]
    dataset_data = {}
    gsm8k_threshold = None

    for name in datasets:
        path = args.data_dir + "/" + name + ".json"
        all_probs, labels, f1_best, best_thr = evaluate_dataset(
            path, model, tokenizer, device, name, args.max_length
        )
        dataset_data[name] = (all_probs, labels)

        if name == "gsm8k":
            gsm8k_threshold = best_thr
            print(f"\nGSM8K best threshold: {gsm8k_threshold:.2f}")

    print("\n=== FINAL RESULTS WITH GSM8K THRESHOLD ===")
    for name, (all_probs, labels) in dataset_data.items():
        f1_strict, ae_strict, ac_strict = compute_metrics(all_probs, labels, gsm8k_threshold)
        f1_binary, ae_binary, ac_binary = compute_binary_metrics(all_probs, labels, gsm8k_threshold)
        print(f"{name}:")
        print(f"  STRICT F1 = {f1_strict*100:.1f}%  (acc_error={ae_strict*100:.1f}, acc_correct={ac_strict*100:.1f})")
        print(f"  BINARY F1 = {f1_binary*100:.1f}%  (acc_error={ae_binary*100:.1f}, acc_correct={ac_binary*100:.1f})")

    print("\n=== SUMMARY - strict F1 at GSM8K threshold ===")
    strict_f1s = []
    for name, (all_probs, labels) in dataset_data.items():
        f1, ae, ac = compute_metrics(all_probs, labels, gsm8k_threshold)
        strict_f1s.append(f1 * 100)
        print(f"  {name:<20} F1={f1*100:.1f}%")
    print(f"  {'Average':<20} F1={sum(strict_f1s)/len(strict_f1s):.1f}%")


if __name__ == "__main__":
    main()
